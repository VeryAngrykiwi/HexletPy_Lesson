'''
задача:
Реализуйте функцию binary_sum(), которая принимает на вход два двоичных числа в виде строк и возвращает их сумму. Вычисленная сумма также должна быть бинарным числом в виде строки:
'''

#chatGPT
def binary_sum(binary1, binary2):
  # Преобразуем двоичные числа в целочисленное представление
  num1 = int(binary1, 2)
  num2 = int(binary2, 2)

  # Вычисляем сумму целочисленных представлений
  result = num1 + num2

  # Преобразуем результат обратно в двоичное представление и убираем префикс '0b'
  binary_result = bin(result)[2:]

  return binary_result

# Пример использования функции
binary_number1 = "1010"
binary_number2 = "1101"

result = binary_sum(binary_number1, binary_number2)
print(result)

'''
объяснение
Эта функция принимает два двоичных числа в виде строк (binary1 и binary2), преобразует их в целочисленное представление, вычисляет их сумму, а затем возвращает результат в виде строки, представляющей двоичное число.

Пример использования функции демонстрирует, как сложить два двоичных числа "1010" и "1101". Результат будет "10111", что соответствует сумме 10 и 13 в десятичной системе.
'''

#replit
def binary_sum(a, b):
  return bin(int(a, 2) + int(b, 2))[2:]

print(binary_sum('1101', '101'))